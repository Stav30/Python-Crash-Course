#4-10: Slices
# print the first 3 items in the list are
my_foods = ['pizza', 'falafel', 'carrot cake', 'canoli', 'ice cream']
print("The first 3 items are : ")
print(my_foods[:3])
#print the middle 3 items are
print("The middle 3 items are : ")
print(my_foods[1:4])
#print The last 3 items are
print("The last 3 items are: ")
print(my_foods[2:5])
